// crea un arrow function llamada saludoPersonalizado que 
// reciba dos parametros: nombre y edad, y retorne una cadena como la siguiente: "hola, me llamo Isay y tengo 37 años.

const saludoPersonalizado = (nombre, edad) => "Hola, me llamo " + nombre + " y tengo " + edad + " años.";

console.log(saludoPersonalizado("Isay", 37)); 
console.log(saludoPersonalizado("Toiler", 24));
console.log(saludoPersonalizado("Maria", 20));